function imgChanger() {
    var anyImg = document.querySelectorAll('img');
    anyImg.forEach((change) => {change.src = "http://bit.ly/lpgreenfox"});
}

function allImgChanger() {
    var allImg = window.document.querySelectorAll('img');
    allImg.forEach((all) => {all.src = "http://bit.ly/lpgreenfox"});
}
